print("Este programa calcula o perimetro de um retângulo com os dados que você digitar")

lado_retangulo = float(input("Digite o lado do retangulo: "))
base_retangulo = float(input("Digite a base do retangulo: "))

perimetro_retangulo = (2 * lado_retangulo) * (2 * base_retangulo)

print("O perimetro do retângulo é: " +str(perimetro_retangulo))